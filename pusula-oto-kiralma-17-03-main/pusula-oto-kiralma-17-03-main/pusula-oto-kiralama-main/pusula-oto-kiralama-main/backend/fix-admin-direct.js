
const sql = require('mssql');
const bcrypt = require('bcrypt');

async function fixAdmin() {
    const config = {
        user: 'rentacar_user',
        password: 'RentUser12345',
        server: 'localhost',
        port: 1443,
        database: 'RentACar',
        options: {
            encrypt: false,
            trustServerCertificate: true
        }
    };

    try {
        console.log("Connecting...");
        await sql.connect(config);
        console.log("Connected.");

        // Check if user exists
        const userCheck = await sql.query`SELECT * FROM users WHERE email = 'admin@rentacar.com'`;

        if (userCheck.recordset.length === 0) {
            console.log("User NOT found. Creating new Admin user...");
            const hash = await bcrypt.hash('admin123', 10);

            // Assuming ID is auto-generated by DB (NEWID()) or we let TypeORM handle it. 
            // If DB column has DEFAULT NEWID(), we can omit ID. 
            // Let's try omitting first. If error, we supply UUID.

            // Use parameterized query for safety/syntax
            const request = new sql.Request();
            request.input('email', sql.NVarChar, 'admin@rentacar.com');
            request.input('hash', sql.NVarChar, hash);
            request.input('name', sql.NVarChar, 'Admin User');
            request.input('role', sql.NVarChar, 'ADMIN');

            // Note: If ID is not default, this might fail. 
            // Safe bet: Generate a UUID if we can, or rely on DB default.
            // Let's try simple INSERT specifying columns.

            try {
                await request.query(`
                    INSERT INTO users (email, password_hash, full_name, role, created_at, updated_at) 
                    VALUES (@email, @hash, @name, @role, GETDATE(), GETDATE())
                `);
                console.log("Admin user created successfully.");
            } catch (insertErr) {
                console.log("Insert failed (maybe missing ID?):", insertErr.message);
                // Fallback: Generate UUID
                const crypto = require('crypto');
                const uuid = crypto.randomUUID();
                request.input('id', sql.NVarChar, uuid);

                await request.query(`
                    INSERT INTO users (id, email, password_hash, full_name, role, created_at, updated_at) 
                    VALUES (@id, @email, @hash, @name, @role, GETDATE(), GETDATE())
                `);
                console.log("Admin user created successfully with Client-side UUID.");
            }

        } else {
            console.log("User found. Updating role...");
            await sql.query`UPDATE users SET role = 'ADMIN' WHERE email = 'admin@rentacar.com'`;
            console.log("User updated to ADMIN.");
        }
    } catch (err) {
        console.error("Error:", err);
    } finally {
        await sql.close();
    }
}

fixAdmin();
